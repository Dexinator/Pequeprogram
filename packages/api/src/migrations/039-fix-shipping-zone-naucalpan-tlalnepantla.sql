--    Se resuelven las zonas por zone_code (no por id hardcodeado) para que funcione igual
--    en local, staging y producción.
UPDATE shipping_zone_postcodes
SET zone_id = (SELECT id FROM shipping_zones WHERE zone_code = 'edomex2')
WHERE zone_id = (SELECT id FROM shipping_zones WHERE zone_code = 'edomex1')
  AND postal_code NOT IN (SELECT postal_code FROM naucalpan_tlalnepantla_cps);

-- 2. Meter en la zona $95 (edomex1) los CPs reales de Naucalpan/Tlalnepantla que estaban en edomex2
UPDATE shipping_zone_postcodes
SET zone_id = (SELECT id FROM shipping_zones WHERE zone_code = 'edomex1')
WHERE zone_id = (SELECT id FROM shipping_zones WHERE zone_code = 'edomex2')
  AND postal_code IN (SELECT postal_code FROM naucalpan_tlalnepantla_cps);

